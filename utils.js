
// export and declare functions per calculator function //
export function getSum(firstNumber, secondNumber) {
    return firstNumber + secondNumber;
}

export function getDifference(firstNumber, secondNumber) {
    return firstNumber - secondNumber;
}

export function getProduct(firstNumber, secondNumber) {
    return firstNumber * secondNumber;
}

export function getQuotient(firstNumber, secondNumber) {
    return firstNumber / secondNumber;
}


